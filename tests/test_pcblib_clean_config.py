from pathlib import Path
import re

from altium_cruncher.altium_cruncher_cmd_clean import _write_config_template
from altium_cruncher.altium_pcblib_clean import (
    DEFAULT_PCBLIB_CLEAN_CONFIG_FILENAME,
    PcbLibCleanConfig,
    find_workspace_pcblib_clean_config_path,
)
from altium_cruncher.config_json import load_json_config


def _assert_jsonc_properties_are_commented(text: str) -> None:
    lines = text.splitlines()
    property_pattern = re.compile(r'^\s+"[^"]+":')
    failures: list[str] = []
    for index, line in enumerate(lines):
        if property_pattern.match(line) is None:
            continue
        previous = index - 1
        while previous >= 0 and not lines[previous].strip():
            previous -= 1
        if previous < 0 or not lines[previous].lstrip().startswith("//"):
            failures.append(line.strip())
    assert failures == []


def test_find_workspace_pcblib_clean_config_path_from_project_child(tmp_path: Path) -> None:
    config_dir = tmp_path / "config"
    config_dir.mkdir()
    (config_dir / "workspace.json").write_text("{}", encoding="utf-8")

    child_dir = tmp_path / "project" / "board"
    child_dir.mkdir(parents=True)

    assert find_workspace_pcblib_clean_config_path(start_dir=child_dir, env={}) == (
        config_dir / DEFAULT_PCBLIB_CLEAN_CONFIG_FILENAME
    )


